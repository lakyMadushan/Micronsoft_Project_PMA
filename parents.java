    private void idLoad() {
        try {
            ResultSet rs = DB.search("select id from parent order by id DESC");
            if (rs.next()) {
                jTextField1.setText("par-" + String.valueOf(Integer.parseInt(rs.getString("id")) + 1));
            } else {
                jTextField1.setText("par-1");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void tableLoad() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = DB.search("select * from parent order by id DESC");
            while (rs.next()) {
                Vector<Object> v = new Vector<>();
                v.add(rs.getString("s_g_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("phone"));
                v.add(rs.getString("nic"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Table loading Unsuccesfully !", "ERROR", JOptionPane.INFORMATION_MESSAGE, erricon);
        }
    }

    private void clear() {
        ((DefaultTableModel) jTable1.getModel()).setNumRows(0);
        idLoad();
        jButton2.setEnabled(true);
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }
    
