    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            if (jTable1.getSelectedRow() != -1) {

                String rid = dtm.getValueAt(jTable1.getSelectedRow(), 0).toString();
                String name = dtm.getValueAt(jTable1.getSelectedRow(), 1).toString();
                String phone = dtm.getValueAt(jTable1.getSelectedRow(), 2).toString();
                String nic = dtm.getValueAt(jTable1.getSelectedRow(), 3).toString();

                jTextField1.setText(rid);
                jTextField2.setText(name);
                jTextField3.setText(phone);
                jTextField4.setText(nic);
                jButton2.setEnabled(false);
            } else {
                System.out.println("No data found for ID: " + jTextField1.getText());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked

