    private void jTextField7KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField7KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                ResultSet rs = DB.search("select * from entrence_t where id = '" + jTextField7.getText() + "'");
                rs.next();
                if (rs.getString("p_status").equals("done")) {
                    JOptionPane.showMessageDialog(this, "Already Paid", "ERROR", JOptionPane.ERROR_MESSAGE, erricon);
                    jTextField7.setText(null);
                    jTextField7.grabFocus();
                } else {

                    //time range
                    //ResultSet rs = DB.search("select * from entrence_t where id = '" + jTextField7.getText() + "'");
                    //rs.next();
                    jTextField8.setText(rs.getString("time"));
                    jTextField9.setText(jLabel1.getText()); //get time range

                    String str1 = jTextField8.getText();
                    String str2 = jTextField9.getText();
                    //String fix3 = jLabel9.getText();
                    ;
                    SimpleDateFormat sdf10 = new SimpleDateFormat("hh.mm.ss.aa"); //.aa
                    Date d1 = sdf10.parse(str1);
                    Date d2 = sdf10.parse(str2);
                    //Date fix = sdf10.parse(fix3);
                    long diff = d2.getTime() - d1.getTime(); //- fix.getTime();
                    String t = String.valueOf((diff / (1000 * 60 * 60)));
                    jTextField10.setText(t);

                    //cost calculate
                    ResultSet rs1 = DB.search("select * from entrence_t where id = '" + jTextField7.getText() + "'");
                    ResultSet rs2 = DB.search("select * from play_a");
                    ResultSet rs3 = DB.search("select * from move_a");
                    rs1.next();
                    rs2.next();
                    rs3.next();
                    if (rs1.getString("s_area").equals("Play Area")) {
                        double p1 = Double.parseDouble(rs2.getString("e_fee"));
                        double p2 = Double.parseDouble(rs2.getString("ex_fee"));

                        jTextField13.setText(String.valueOf(p1));

                        //time(hours) * extra fees
                        double t1;
                        t1 = Double.parseDouble(t);
                        double caltime = p2 * t1;
                        jTextField14.setText(String.valueOf(caltime));

                    } else if (rs1.getString("s_area").equals("Move Area")) {
                        double m1 = Double.parseDouble(rs3.getString("e_fee"));
                        double m2 = Double.parseDouble(rs3.getString("ex_fee"));

                        jTextField13.setText(String.valueOf(m1));

                        //time(hours) * extra fees
                        double t1;
                        t1 = Double.parseDouble(t);
                        double caltime = m2 * t1;
                        jTextField14.setText(String.valueOf(caltime));
                    }

                    //total calculate
                    double efee = Double.parseDouble(jTextField13.getText());
                    double exfee = Double.parseDouble(jTextField14.getText());
                    double calfee = efee + exfee;
                    jTextField15.setText(String.valueOf(calfee));

                    jTextField16.requestFocus();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTextField7KeyPressed

