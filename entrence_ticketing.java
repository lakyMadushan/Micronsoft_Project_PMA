    public void idload() {
        try {
            ResultSet rs = DB.search("select id from entrence_t order by id desc");
            if (rs.next()) {
                String id = rs.getString("id");
                int id1 = Integer.parseInt(id) + 1;
                jTextField1.setText(String.valueOf(id1));

            } else {
                jTextField1.setText("1");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void tableload() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs = DB.search("select * from entrence_t where p_num like '%" + jTextField3.getText() + "%'");
            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("date"));
                v.add(rs.getString("time"));
                v.add(rs.getString("p_num"));
                v.add(rs.getString("k_name"));
                v.add(rs.getString("s_area"));
                v.add(rs.getString("status"));
                v.add(rs.getString("p_status"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

